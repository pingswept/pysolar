    if year < delta_t_base_year :
        year = delta_t_base_year
        month = 1
    elif year == delta_t_base_year :
        month = max(0, month - delta_t_base_month) + 1
    elif year >= delta_t_base_year + len(delta_t) :
        year = delta_t_base_year + len(delta_t) - 1
    #end if
